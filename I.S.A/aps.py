''' This module takes input from user of two numbers 
and performs arithmetic operation of division and prints them. '''
def main():
    ''' This is the main function. '''
    num1 = int(input('Enter the first number:'))
    num2 = int(input('Enter the second number:'))
    print(num1//num2)
    print(num1/num2)
if __name__ == "__main__":
    main()
